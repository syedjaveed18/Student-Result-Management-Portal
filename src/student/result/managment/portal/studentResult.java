    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno = jTextField2.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentResult", "root", "rootpassword");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join result where student.rollno='"+rollno+"' and result.rollno='"+rollno+"'");
            if(rs.next()){
                jTextField3.setText(rs.getString(4));
                jTextField4.setText(rs.getString(6));
                jTextField5.setText(rs.getString(2));
                jTextField6.setText(rs.getString(3));
                jTextField7.setText(rs.getString(5));
//                jTextField3.setText("e");
//                jTextField4.setText("d");
//                jTextField5.setText("c");
//                jTextField6.setText("b");
//                jTextField7.setText("a");
                
                jTextField1.setText(rs.getString(8));
                jTextField8.setText(rs.getString(9));
                jTextField9.setText(rs.getString(10));
                jTextField10.setText(rs.getString(11));
                jTextField11.setText(rs.getString(12));
                
                int m1 = Integer.parseInt(jTextField1.getText());
                int m2 = Integer.parseInt(jTextField8.getText());
                int m3 = Integer.parseInt(jTextField9.getText());
                int m4 = Integer.parseInt(jTextField10.getText());
                int m5 = Integer.parseInt(jTextField11.getText());
                
                int total = m1+m2+m3+m4+m5;
                jTextField12.setText(String.valueOf(total));
                
                if(m1 < 30 || m2 < 30 || m3 < 30 || m4 < 30 || m5 < 30){
                    jTextField13.setText("Fail");
                    jLabel28.setText("Better luck next time");
                } else {
                    jTextField13.setText("Pass");
                    jLabel28.setText("Congrulations!");
                }
                double per = (total/5.0);
                jTextField14.setText(String.valueOf(per)+"%");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

