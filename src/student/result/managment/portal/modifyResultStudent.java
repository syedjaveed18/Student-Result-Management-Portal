    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String phy = jTextField2.getText();
        String chem = jTextField3.getText();
        String math = jTextField4.getText();
        String em = jTextField5.getText();
        String dbms = jTextField6.getText();

        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentResult","root","rootpassword");

            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from result where rollno='"+rollno+"'");

            if(rs.next()){
                st.executeUpdate("update result set physics = '"+phy+"',chemisrty = '"+chem+"',maths = '"+math+"',em = '"+em+"',dbms = '"+dbms+"' where rollno='"+rollno+"'");
                //st.executeUpdate("delete from student where rollno='"+rollno+"'");
                JOptionPane.showMessageDialog(null, "Result Modified Successfully");
                setVisible(false);
                new allStudentResult().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Roll number does not exist.");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed
   
