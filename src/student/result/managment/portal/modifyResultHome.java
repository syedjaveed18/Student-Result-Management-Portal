    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentResult","root","rootpassword");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from result where rollno= '" +rollno+ "'");

            if(rs.next()){
                setVisible(false);
                new modifyResultStudent(rollno).setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Roll number does not exists");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

