    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/studentResult","root","rootpassword");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno= '" +rollno+ "'");

            if(rs.next()){
                int res = JOptionPane.showConfirmDialog(null, "Confirm, do yo want to delete student from registered list ?");
                if(res == 0){
                    st.executeUpdate("delete from student where rollno= '"+rollno+"'");
                    //JOptionPane.showConfirmDialog(rootPane, res, rollno, WIDTH);
                    JOptionPane.showMessageDialog(null, "Student Deleted Succesfully");
                    setVisible(false);
                    new registeredStudents().setVisible(true);
                } else {
                    setVisible(false);
                    new registeredStudents().setVisible(true);
                }
                
            } else {
                JOptionPane.showMessageDialog(null, "Roll number does not exists");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

